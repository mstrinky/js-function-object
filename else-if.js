// else if 

var color = 'orange';
if (color=='red'){
    console.log('you are my red friend');
}
else if (color=='white'){
    console.log('you are my white friend');
}
else if (color=='orange'){
    console.log('you are my orange friend');
}
else {
    console.log('you are my black friend');
}

